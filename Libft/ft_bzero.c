#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*ptr;
	int				x;

	ptr = (unsigned char *) s;
	x = 0;
	while (n > 0)
	{
		ptr[x] = 0;
		x++;
		n--;
	}
}
